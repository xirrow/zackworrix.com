            <h1>EVENT INFO</h1>
            <h6><a href='#details'>Skip to COURSE DETAILS</a></h6>
            
            <p>The Annual Ace in the Hole MultiSport Weekend is a legendary event in the Oregon triathlon and running community. It has become a traditional destination race for athletes from across the nation.</p> 
            
            <p>There is something for every level of athletic ability. The weekend includes a first timer triathlon, a sprint, Olympic, and Half-Iron triathlons and 10K and Half marathon runs. Come to experience your first race or come to compete to win, but make sure you come to have fun!</p>
            
            <h2>What to Bring</h2>
            
            <p>Watch the weather closely.  The show goes on no matter what the weather is doing.</p> 
            
            <p>You must wear your chip timing piece during the entire event.  You will be given a band that will hold your chip timing piece around your ankle throughout the entire event. Be sure that it is snapped tightly. Be sure to have your Chip Timing piece on before you start the race and be sure to step over the timing mats after each segment of the race. If you are wearing a wet-suit, make sure the timing piece goes under your wetsuit otherwise, you will have a very difficult time getting off your wet suit.</p>

            <p><b>Swim:</b>  Wetsuits are optional for the swim but will provide buoyancy and warmth. However, many people opt for no wetsuit for a triathlon so no worries.  We will provide you with a swim cap but you will want to bring your own goggles.</p>

            <p><b>Bike:</b>  A biking helmet is mandatory.  You will also receive 2 stickers in your package with your race number on them.  The small sticker should go on the front of your helmet.  The bigger sticker will wrap around your bike frame.  Road or mountain bikes are acceptable.  For safety reasons, be sure to have end-caps on the end of your handle-bars.</p>

            <p><b>Run:</b>  You must finish the race with your bib number on the front of you.  Some people choose to pin it on at the beginning of the race and have it on for the whole event so they don't have to worry about it.  Others pin it on a singlet that they put on once they finish the swim before they head out for the bike.  Others use an elastic racing strap that they pin their bib number to and then quickly strap it on before they leave for the run.  Use whatever option feels best for you.</p>

            <p>Remember to bring a change of clothing so you can enjoy the post-event festivities.</p>
            
            <h1 id="details">COURSE DETAILS</h1>
            
            <p><b>WATER TEMPERATURE is expected to be between 62 - 66 degrees.</b></p> 
            <p>The temperature will be taken on Friday and the morning of the race. Wetsuits are recommended.</p>
            
            <br>
            <h3 class="courses"><b>Long Course - Event begins Saturday at 7:00AM</b></h3>

                <p><b>Long Course SWIM - 1.2 Miles:</b> Participants will make two counter-clockwise loops. Large buoys will mark the turn points. Kayakers will be positioned on the water to support the swimmers. Medical support will be present on the beach.</p>

                <p><b>Long Course BIKE - 58 Miles:</b> A scenic point-to-point course that travels over gently rolling hills prior to three hard climbs. The bike course will be marked with large directional signage and there will be course marshals at key intersections to help direct you.</p> 

                <p><b>Long Course RUN - 13.1 Miles:</b> A mostly flat loop course on widely paved bike paths that traverse through and around this beautiful and scenic destination resort (two hills total with a minimal elevation gain).</p>
            
            <h3 class="courses"><b>Olympic Course - Event begins Saturday at 7:30AM</b></h3>
            
                <p><b>OLYMPIC SWIM - 1,500 Meters:</b> Participants will make two counter-clockwise loops. Large buoys will mark the turn points. Kayakers will be positioned on the water to support the swimmers. Medical support will be present on the beach.</p>

                <p><b>OLYMPIC BIKE - 28 Miles:</b> A scenic point-to-point course that travels over gently rolling hills. The bike course will be marked with large directional signage and course marshals will be present at key intersections.</p>

                <p><b>OLYMPIC RUN - 10 Kilometer (6.21 Miles):</b> A mostly flat loop course on widely paved bike paths that traverse through and around this beautiful and scenic destination resort (one hill total with a minimal elevation gain).</p>
            
            <br>
            <h3 class="courses"><b>More great events!</b></h3>
            
                <h4 class="courses"><b>Saturday:</b></h4>
           
                <p class="courses-indi"><b>10K (6.21 Miles) Course RUN - Begins at 7:15AM:</b> The 10K event starts and finishes in the Athletes Village. The paths are approximately 6 ft wide, perfectly paved and wind around through the forest. Each course has only two small hills with a minimal elevation gain to navigate and a fast downhill to flat finish to the roaring cheers of the crowd.</p>
               
                <p class="courses-indi"><b>Half Marathon (13.1 Miles) Course RUN  - Begins at 7:15AM:</b> Half-Marathon event starts and finishes in the Athletes Village to the cheers of the enthusiastic crowd. Once finished, runners can enjoy the finish line festivities, including the Sports &amp; Fitness Expo and live entertainment. Post-race refreshments will be provided and the Awards Ceremony for the Half-Marathon will begin once the results have been certified.</p>
                
                <h4 class="courses"><b>Sunday:</b></h4>
                
                <p class="courses-indi"><b>Sprint - Begins at 8:00AM:</b> Course will offer a 1-loop 1/2 mile swim, the exact same 28 mile Bike Course as the Olympic distance and a 5km run.</p>

                <p class="courses-indi"><b>Try-A-Tri - Begins at 8:20AM:</b> This novice race is designed for the first time triathlete, those new to the sport, our Junior Triathletes. The swim is a shorter, more manageable 1/4 mile distance, (instead of the standard 1/2-mile Sprint distance swim), 10 mile bike ride (vs 12 miles and it's a 2 loop course making it very spectator friendly!) and a flat 2 mile run (vs 3 mile sprint course).</p>

